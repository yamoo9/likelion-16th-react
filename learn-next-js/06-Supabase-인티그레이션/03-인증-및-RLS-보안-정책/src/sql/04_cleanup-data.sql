-- 데이터 초기화 및 삭제 관련 (04_cleanup-data.sql)
-- 테이블의 구조는 남겨두고 데이터만 싹 비우고 싶을 때 사용합니다. (CSV 재임포트 시 유용)

-- [1] 테이블의 모든 데이터 삭제 (ID 시퀀스 초기화 포함)
-- 실습 중 데이터가 꼬였을 때 가장 많이 사용합니다.
-- 'restart identity'는 자동 증가하는 ID 번호를 다시 1번부터 시작하게 만듭니다.
truncate table <테이블_이름> restart identity;

-- [2] 특정 사용자의 데이터만 골라서 삭제
-- '본인의_UUID_입력' 자리에 02번 과정에서 복사한 UUID를 넣으세요.
delete from <테이블_이름> 
where user_id = '본인의_UUID_입력';

-- [3] 테이블 자체를 완전히 삭제 (주의!)
-- 테이블 구조까지 모두 삭제되므로 신중하게 사용하세요. 필요할 때만 주석(--)을 풀고 실행합니다.
-- drop table <테이블_이름>;