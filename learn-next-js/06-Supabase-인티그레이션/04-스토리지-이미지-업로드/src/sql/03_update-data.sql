-- 데이터 수정 및 소유권 변경 (03_update-data.sql)
-- 이미 들어있는 데이터의 주인을 나로 바꾸거나 내용을 일괄 수정할 때 사용합니다.

-- [1] 모든 메모의 주인을 현재 로그인한 내 계정으로 변경
-- CSV 임포트 후 user_id가 비어있거나 다를 때 사용하세요.
-- 주의: '본인의_UUID_입력' 자리에 02번 과정에서 복사한 UUID를 넣으세요.
update <테이블_이름> 
set user_id = '본인의_UUID_입력';

-- [2] 특정 ID를 가진 메모의 내용 수정
-- id가 숫자형(int)인 경우와 UUID형인 경우에 맞춰 where 조건을 사용하세요.
update <테이블_이름> 
set content = '수정된 내용입니다.'
where id = '수정할_데이터의_ID';

-- [3] 특정 유저의 메모 개수 확인 (통계)
-- 내가 소유한 데이터가 몇 개인지 확인할 때 유용합니다.
select count(*) 
from <테이블_이름> 
where user_id = '본인의_UUID_입력';